class User:
    def __int__(self, first_name, second_name, last_name, phone, email, birthday, series_num, login,
                password, data, id_group):
        self.first_name = first_name
        self.second_name = second_name
        self.last_name = last_name
        self.phone = phone
        self.email = email
        self.birthday = birthday
        self.series_num = series_num
        self.login = login
        self.password = password
        self.date = data
        self.id_group = id_group
